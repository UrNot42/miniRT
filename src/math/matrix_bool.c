#include "minirt.h"

bool	matr_2_eq(t_2mtrx a, t_2mtrx b)
{
	return (f_eq(a[0][0], b[0][0]) && f_eq(a[0][1], b[0][1])
		&& f_eq(a[1][0], b[1][0]) && f_eq(a[1][1], b[1][1]));
}

bool	matr_3_eq(t_3mtrx *a, t_3mtrx *b)
{
	return (f_eq((*a)[0][0], (*b)[0][0]) && f_eq((*a)[0][1], (*b)[0][1])
		&& f_eq((*a)[0][2], (*b)[0][2])
		&& f_eq((*a)[1][0], (*b)[1][0]) && f_eq((*a)[1][1], (*b)[1][1])
		&& f_eq((*a)[1][2], (*b)[1][2])
		&& f_eq((*a)[2][0], (*b)[2][0]) && f_eq((*a)[2][1], (*b)[2][1])
		&& f_eq((*a)[2][2], (*b)[2][2]));
}

bool	matr_4_eq(t_4mtrx a, t_4mtrx b)
{
	return (f_eq(a[0][0], b[0][0]) && f_eq(a[0][1], b[0][1])
		&& f_eq(a[0][2], b[0][2]) && f_eq(a[0][3], b[0][3])
		&& f_eq(a[1][0], b[1][0]) && f_eq(a[1][1], b[1][1])
		&& f_eq(a[1][2], b[1][2]) && f_eq(a[1][3], b[1][3])
		&& f_eq(a[2][0], b[2][0]) && f_eq(a[2][1], b[2][1])
		&& f_eq(a[2][2], b[2][2]) && f_eq(a[2][3], b[2][3])
		&& f_eq(a[3][0], b[3][0]) && f_eq(a[3][1], b[3][1])
		&& f_eq(a[3][2], b[3][2]) && f_eq(a[3][3], b[3][3]));
}
